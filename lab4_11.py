
import turtle
t = turtle.Turtle()

def position_turtle(x, y):
  """
  possitions the start possition of the turtle to (x, y)
  """
  t.hideturtle()
  t.penup()
  t.goto(x, y)
  t.showturtle()
  t.pendown()
  
